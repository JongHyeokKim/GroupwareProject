package com.groupware.dto.address;

public class AddressViewVO {
	private String mem_num;
	private String mem_nm;
	private String mem_em; 
	private String mem_tel;
	private String mem_img;
	private String pos_nm;
	private String dep_code;
	private String dep_nm;
	
	public String getMem_num() {
		return mem_num;
	}
	public void setMem_num(String mem_num) {
		this.mem_num = mem_num;
	}
	public String getMem_nm() {
		return mem_nm;
	}
	public void setMem_nm(String mem_nm) {
		this.mem_nm = mem_nm;
	}
	public String getDep_code() {
		return dep_code;
	}
	public void setDep_code(String dep_code) {
		this.dep_code = dep_code;
	}
	public String getDep_nm() {
		return dep_nm;
	}
	public void setDep_nm(String dep_nm) {
		this.dep_nm = dep_nm;
	}
	public String getMem_em() {
		return mem_em;
	}
	public void setMem_em(String mem_em) {
		this.mem_em = mem_em;
	}
	public String getMem_tel() {
		return mem_tel;
	}
	public void setMem_tel(String mem_tel) {
		this.mem_tel = mem_tel;
	}
	public String getPos_nm() {
		return pos_nm;
	}
	public void setPos_nm(String pos_nm) {
		this.pos_nm = pos_nm;
	}
	public String getMem_img() {
		return mem_img;
	}
	public void setMem_img(String mem_img) {
		this.mem_img = mem_img;
	}
	
}
