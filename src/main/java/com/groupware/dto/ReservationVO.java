package com.groupware.dto;

public class ReservationVO {
	
	private String reserv_code;
	private String reserv_day;
	private String reserv_time;
	private String reserv_fac_code;
	private String reserv_mem_num;
	private String fac_name;
	private String mem_nm;
	
	
	public String getReserv_code() {
		return reserv_code;
	}
	public void setReserv_code(String reserv_code) {
		this.reserv_code = reserv_code;
	}
	public String getReserv_day() {
		return reserv_day;
	}
	public void setReserv_day(String reserv_day) {
		this.reserv_day = reserv_day;
	}
	public String getReserv_time() {
		return reserv_time;
	}
	public void setReserv_time(String reserv_time) {
		this.reserv_time = reserv_time;
	}
	public String getReserv_fac_code() {
		return reserv_fac_code;
	}
	public void setReserv_fac_code(String reserv_fac_code) {
		this.reserv_fac_code = reserv_fac_code;
	}
	public String getReserv_mem_num() {
		return reserv_mem_num;
	}
	public void setReserv_mem_num(String reserv_mem_num) {
		this.reserv_mem_num = reserv_mem_num;
	}
	public String getFac_name() {
		return fac_name;
	}
	public void setFac_name(String fac_name) {
		this.fac_name = fac_name;
	}
	public String getMem_nm() {
		return mem_nm;
	}
	public void setMem_nm(String mem_nm) {
		this.mem_nm = mem_nm;
	}
	
	

}
